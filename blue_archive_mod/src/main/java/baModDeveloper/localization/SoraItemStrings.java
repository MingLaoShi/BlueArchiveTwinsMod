package baModDeveloper.localization;

public class SoraItemStrings {
    public String name;
    public String description;
    public String message;
    public int price;
    public int count;

    public SoraItemStrings(String name, String description, String message, int price, int count) {
        this.name = name;
        this.description = description;
        this.message = message;
        this.price = price;
        this.count = count;
    }
}
